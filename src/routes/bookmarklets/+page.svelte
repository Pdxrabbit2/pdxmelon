<h1>
    This is the bookmarklets page. There are some bookmarklets here and an explanation what bookmarklets are.
</h1>
<p>
    Bookmarklets run javascript on a given page when a bookmark is clicked in the browser.
    They can do a lot of things. For example, they can make all the text on the page editable, 
    they can also do things like change site functionality. You can make a bookmarklet by changing
    the url of a bookmark to "javascript: code goes here". You would not include the double quotes.
</p>
